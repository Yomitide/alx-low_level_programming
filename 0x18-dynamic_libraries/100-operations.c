/**
 * add - to (+) two numbers.
 *
 * @a:first number.
 * @b:second number.
 *
 * Return: a+b;
 */
int add(int a, int b)
{
	return (a + b);
}


/**
 * sub - to (-) two numbers.
 *
 * @a:first number.
 * @b:second number.
 *
 * Return: a-b;
 */
int sub(int a, int b)
{
	return (a - b);
}


/**
 * mul - to (*) two numbers.
 *
 * @a:first number.
 * @b:second number.
 *
 * Return: a*b;
 */
int mul(int a, int b)
{
	return (a * b);
}


/**
 * div - to (/) two numbers.
 *
 * @a:first number.
 * @b:second number.
 *
 * Return: a/b;
 */
int div(int a, int b)
{
	return (a / b);
}


/**
 * mod - to (%) two numbers.
 *
 * @a:first number.
 * @b:second number.
 *
 * Return: a%b;
 */
int mod(int a, int b)
{
	return (a % b);
}
